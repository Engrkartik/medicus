Indication moderate or severe knee pain while resting, day or night, Chronic knee inflammation and swelling, Knee deformity ,Knee stiffness <br />


<strong>Hip replacement.</strong>. is a procedure in which a doctor surgically removes a painful hip joint with arthritis and replaces it with an artificial joint often made from metal and plastic components.<br />

Alarm sign for hip Replacement Surgery: Bone damage or deformity, Pain, swelling, and stiffness, If constant pain affects your daily activities and mood, it may be time for surgery .

