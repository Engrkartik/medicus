                                <p><strong> General information.. </strong><br />


The kidneys,are two in number & are bean(or rajma) shaped - organs .Their main function is the balance of Electrolytes in the blood, the excretions of of waste products of metabolism . Kidneys are essential to the urinary system  and regulation of blood pressure They also serve the body as a natural filter of the blood, and remove water-soluble wastes to make urine &reached out to the the bladder&comes via the urethra  
<br />
<br />
<img src="images/kidney1.jpg" />

<div class="clearfix"></div>
<strong>Kidney stones (nephrolithiasis):</strong>  are a relatively common and particularly painful disorder. A chronic condition can result in scars to the kidneys,One common typical  symptom of kidney stones is a sharp to disabling pain in the middle and sides of the lower back or groin. <br />


Renal failure, also known as kidney failure or renal insufficiency, is a medical condition in which the kidneys fail to adequately filter waste products from the blood.<br />

The two main forms are ARF or acute kidney injury and chronic kidney disease or CRF & both have usually an underlying cause. <br />

Acute kidney injury (AKI) or  Acute renal failure (ARF).. is a rapidly progressive loss of renal function, generally characterized by  decreased urine production (oliguria)The underlying cause must be identified and treated to stop the progress, <br />

Chronic kidney disease (CKD) or Chronic Renal failure (CRF)..  it  can be the long term consequence of irreversible acute disease or part of a disease progression, initially may show few symptoms of acute disease. 

<br />
<br />
<img src="images/kidney2.jpg" />
<div class="clearfix"></div>

<strong>Causes</strong><br />

Causes of ARF OR AKI include accidents, injuries, or complications from surgeries in which the kidneys are Shut of normal blood flow for extended periods of time, like long Heart-bypass surgery ,. other reason are Drug overdoses, accidental or from chemical overloads of drugs such as antibiotics or chemotherapy or toxic substance , may also cause the onset of acute kidney injury.<br />


<strong>Chronic kidney disease (CKD)</strong>has numerous causes .The common reason  are diabetes mellitus , long-term, uncontrolled hypertension. Polycystic kidney disease is another well-known cause of CKD. <br />
<br />

Overuse of common drugs such as ibuprofen and Paracetamol can also cause chronic kidney damage.,so please consult your doctor before using these drugs.

A kidney stoneor  renal calcululi or nephrolith, is a solid piece of substance  which is formed in the kidneys from minerals in urine .Kidney stones typically leave the body in the urine stream, and a small stone may pass without causing symptoms .but large stone can blockage the ureter & that leads to flank or lower back pain, often goes to the groin part. Other associated symptoms include dizziness,nausea, fever, vomiting, blood or pus in urine  and difficult to pass urine. sometime  Blockage of the ureter can cause edema or swelling of kidney (hydronephrosis).<br />
<br />


<img src="images/kidney3.jpg" />
<div class="clearfix"></div>

Associated  risk factors include being long sitting, physical inactivity , overweight, not drinking enough fluids certain foods, some medications, and The diagnosis is usually based on symptoms, urine testing, and some investigation like X-Ray ,USG Abdomen KUB,CT-KUB as per requirement <br />
<br />
<img src="images/kidney4.jpg" class="img-responsive" />
<div class="clearfix"></div>
Bilateral kidney stones can be seen on this CT-KUB Film. <br />


<strong>Management.. </strong>
Management of pain often requires  administration  of NSAIDs(treatment of choice or emergency management) or opioids.
Expulsion therapy<br />

The use of medications like diuretics in fluids to speed the spontaneous passage of ureteral calculi is referred to as medical expulsive therapy ,its  just like a give &trial based therapy.<br />

<strong>Lithotripsy</strong>
<img src="images/kidney5.jpg" class="img-responsive" />
<div class="clearfix"></div>

<strong>Extracorporeal shock wave lithotripsy (ESWL)</strong> is a noninvasive technique for the removal of kidney stones. <br />
<br />

<strong>Surgery </strong><br />
<br />


Most stones under 5 mm (0.2 in) pass spontaneously, but in some condition Prompt surgery may, nonetheless, be required in persons with only one working kidney, bilateral obstructing stones, a urinary tract infection and thus, it is presumed, an infected kidney, or intractable pain.
Ureteroscopic  technique has become increasingly popular it  involves the placement of a ureteral stent (a small tube extending from the bladder, up the ureter and into the kidney) to provide immediate relief of an obstructed kidney. It also useful for saving a kidney at risk for swelling,infection that leads to renal failure 

<strong>A urinary tract infection (UTI)</strong> is an infection that affects part of the urinary tract i.e kidney ,ureter bladder or may be whole tract.<br />

The most common symptoms are pain with burning sensation  while passing urine and also  having to urinate frequently (or an urge to urinate) . 
