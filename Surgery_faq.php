                                <p> A hernia is the exit of an organ, such as the bowel, through the wall of the cavity in which it normally resides  most commonly they involve the abdomen, specifically the groin. Groin hernias are most common of the inguinal type but may also be femoral.[1] Other hernias include hiatus, incisional, and umbilical hernias,<br />
<br />

Risk factors for the development of a hernia include: smoking, COPD , pregnancy  obesity, , peritoneal dialysis, collagen vascular disease, and previous open appendectomy, among others<br />
<br />
<strong>Types of hernia(Common hernia)</strong>
<div class="clearfix"></div>

<img src="images/hernia1.jpg" class="img-responsive" />

<strong>1.Inguinal hernia..</strong> <br />
          
When part of an internal organ (usually the small intestine) comes out  through a weak point or tear in the peritoneum( the thin abdominal muscular wall holding the abdominal organs in place) resulting in a bulge. Symptoms ..a small bulge in one or both sides of the groin ,pressure in groin , discomfort or  pain, a feeling of weakness etc <br />
<br />

<strong>2.Umbilical hernias..</strong> They involve protrusion of intraabdominal contents through a weakness at the site of passage of the umbilical cord through the abdominal wall.<br /><br />


<strong>3. Femoral hernias..</strong> Femoral hernias occur just below the inguinal ligament, when abdominal contents pass into the weak area at the posterior wall of the femoral canal.<br />
<br />

<strong>4. Incisional hernia..</strong> An incisional hernia occurs when the defect is the result of an incompletely healed surgical wound. <br />
<br />

Management..<br />

Surgery is recommended for some types of hernias to prevent complications like obstruction of the bowel or strangulation of the tissue,most abdominal hernias can be surgically repaired.<br />
<br />

