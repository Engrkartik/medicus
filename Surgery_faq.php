Gallstones or Cholelithiasis are small, pebble-like substances that develop in the gallbladder. Gallstones form when liquid stored in the gallbladder hardens into pieces of stone-like material.<br />

Symptoms of gallstones .. pain in the right upper abdomen , between the shoulder blades & under the right shoulder <br />

People with any of the alarming  symptoms like fever-even low-grade-or chills, yellowish color of the skin or whites of the eyes ,clay-colored stools  & prolonged pain-more than 5 hours associated with nausea and vomiting  should see a doctor immediately.. <br />

Diagnosis.. Blood tests,(CT) scan abdomen, Cholescintigraphy (HIDA scan). <br />

(ERCP).- used to locate and remove stones in the bile ducts. <br />

 Laparoscopic surgery or cholecystomy to remove the gallbladder is the most common treatment  




